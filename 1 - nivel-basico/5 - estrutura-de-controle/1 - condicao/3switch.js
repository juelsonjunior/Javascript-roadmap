const diaSemana = 5;

switch (diaSemana) {
    case 1:
        console.log('Segunda-feira');

        break;
    case 2:
        console.log('Terça-feira');

        break;
    case 3:
        console.log('Quarta-feira');

        break;
    case 4:
        console.log('Quita-feira');

        break;
    case 5:
        console.log('Sexta-feira');

        break;
    case 6:
        console.log('Sabado');

        break;
    case 7:
        console.log('Domingo');

        break;
    default:
        console.log('Dia da semana inexistente');
        break;
}

const trabalho = 'desenvolvedor';

switch (trabalho) {
    case 'programador':
        console.log('Você trabalha como Programador');

        break;
    case 'desenvolvedor':
        console.log('Você trabalha como Desenvolvedor');
        break;
    default:
        console.log('Você não tem trabalho');
        break;
}
